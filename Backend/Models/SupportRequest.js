const express = require('express');
const mongoose = require('mongoose');
require("dotenv").config();

const supportUserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model('SupportUser', supportUserSchema);